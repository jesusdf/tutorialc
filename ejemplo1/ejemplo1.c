/*
  Un comentario en C se escribe entre llaves y asteriscos.
  En este primer ejemplo veremos el programa más corto que podemos hacer: un programa que finaliza.
 */

/*
 * Funcion principal del programa, siempre se llama main.
 * Puede tener especificados parámetros o no.
 * Puede devolver un valor entero (int) o ignorarlo y dejar que devuelva el valor por defecto (void).
 */

/* 
 * void main(void) { }
 * Sería la versión más corta posible de este programa 
 */
int main() {
	/* 
	 * El valor devuelto por la función main lo utiliza el sistema operativo 
	 * para controlar si el programa se ejecutó bien o no.
	 * Un valor 0 indica una ejecución correcta, un valor distinto de 0 indica un problema.
	 */
	/* return 0; */
	return -1;
}

